#include "prototypes.h"

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
        if (str[i] == '!')
            str[i] = '-';
		ft_putchar(str[i]);
		i++;
	}
}
